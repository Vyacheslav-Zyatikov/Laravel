    {
        $request -> validate([
            'name' => ['required', 'string', 'min:3'],
            'feedbackText' => ['string', 'min:4']
        ]);

        $data = json_encode($request->all());
        file_put_contents(public_path('news/userFeedbackData.json'), $data);
        return view('user.feedback', ['status' => 'ok']);
    }

